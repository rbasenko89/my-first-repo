console.log('Цикл від 0 до 9:');
for (let i = 0; i <= 9; i++) {
    console.log(i);
}
console.log('Цикл від 100 до 0 з кроком 10:');
for (let j = 100; j >= 0; j -= 10) {
    console.log(j);
}
//додаток з while 
console.log("Цикл while від 0 до 9:");
let a = 0;
while (a <= 9) {
  console.log(a);
  a++;
}
console.log("Цикл while від 100 до 0 з кроком 10:");
let b = 100;
while (b >= 0) {
  console.log(b);
  b -= 10;
}
// додаток do...while  
console.log("Цикл do...while від 0 до 9:");
let x = 0;
do {
  console.log(x);
  x++;
} while (x <= 9);
console.log("Цикл do...while від 100 до 0 з кроком 10:");
let y = 100;
do {
  console.log(y);
  y -= 10;
} while (y >= 0);
